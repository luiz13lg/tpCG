    void pintarReta(Graphics g, int x, int y){
        if(aux == 0){
            x1 = x;
            y1 = y;
            aux = 1;
        } else{
            x2 = x;
            y2 = y;
            aux = 0;
            cor = paleta.getColor();
            g.setColor(cor);
            g.drawLine(x1, y1, x2, y2);
        }
    }
    
    void pintarCirculo(Graphics g, int x, int y){
        cor = paleta.getColor();
        g.setColor(cor);
        g.drawOval(x, y, 6, 6);
    }
    
    void pintar(Graphics g, int x, int y){  //pintando um pixel
        cor = paleta.getColor();
        g.setColor(cor);
        g.drawLine(x, y, x, y);
    }
    
